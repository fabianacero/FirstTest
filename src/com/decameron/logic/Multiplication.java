package com.decameron.logic;

public class Multiplication {

	/**
	 * Multiply two indicated numbers
	 */
	public double multiply(double firtNumber, double secondNumber){
		return firtNumber * secondNumber;
	}
	
	
	/**
	 * Pow two indicated numbers
	 */
	public double pow(double firtNumber, double secondNumber){
		return Math.pow(firtNumber, secondNumber);
	}
	
}
