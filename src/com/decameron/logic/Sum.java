package com.decameron.logic;

/**
 * @author fabian.acero
 *
 */
public class Sum {

	/**
	 * Sums two integer numbers
	 * @param firtNumber
	 * @param secondNumber
	 * @return integer
	 */
	public double add(double firtNumber, double secondNumber){
		return firtNumber + secondNumber;
	}
	
	/**
	 * Subtract two integer numbers
	 * @param firtNumber
	 * @param secondNumber
	 * @return int
	 */
	public double subs(double firtNumber, double secondNumber){
		return firtNumber - secondNumber;
	}
}
